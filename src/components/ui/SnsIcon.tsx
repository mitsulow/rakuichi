/**
 * Returns a display-friendly platform name.
 */
export function getPlatformLabel(platform: string): string {
  const labels: Record<string, string> = {
    instagram: "Instagram",
    x: "X",
    twitter: "X",
    facebook: "Facebook",
    youtube: "YouTube",
    tiktok: "TikTok",
    note: "note",
    ameblo: "アメブロ",
    line: "LINE",
    threads: "Threads",
    website: "ウェブサイト",
  };
  return labels[platform] ?? platform;
}

/**
 * Auto-detect platform from URL.
 */
export function detectPlatform(url: string): string {
  if (/instagram\.com/.test(url)) return "instagram";
  if (/(?:twitter|x)\.com/.test(url)) return "x";
  if (/facebook\.com/.test(url)) return "facebook";
  if (/youtube\.com|youtu\.be/.test(url)) return "youtube";
  if (/tiktok\.com/.test(url)) return "tiktok";
  if (/note\.com/.test(url)) return "note";
  if (/ameblo\.jp/.test(url)) return "ameblo";
  if (/line\.me/.test(url)) return "line";
  if (/threads\.net/.test(url)) return "threads";
  return "website";
}
